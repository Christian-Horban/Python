# Prompt user for the first number, convert it to an integer, and store it in variable a
a = int(input("Enter the first number: "))

# Prompt user for the second number, convert it to an integer, and store it in variable b
b = int(input("Enter the second number: "))

# Add variables a and b and store the result in variable c
c = a + b

# Print the value of c
print("The sum of the two numbers is:", c)
